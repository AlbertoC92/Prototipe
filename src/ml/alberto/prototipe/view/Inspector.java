    //Method to cahrge users on the tables by and specific id
    public void chargeUsers() {
        
        try {
            String[] headers = {"USERLOGIN", "USERMODIFY", "INFORMATION"};
            String[][] cells;

            
                datas= dat.getDatas();
                
                cells = new String[datas.size()][4];
                for (int i = 0; i < datas.size(); i++) {
                    cells[i][0] = datas.get(i).getUserlogin();
                    cells[i][1] = datas.get(i).getUser();
                    cells[i][2] = datas.get(i).getDatas();
                    

                }
               

                ml.alberto.prototipe.model.TableModel model = new ml.alberto.prototipe.model.TableModel(cells, headers);
                txtDatas.setModel(model);

            

        } catch (NullPointerException npe) {

        }
    }



