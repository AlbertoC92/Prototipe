    private void searchBarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBarKeyTyped
        searchBar.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(final KeyEvent e) {
                String cadena = (searchBar.getText());
                searchBar.setText(cadena);
                repaint();
                filtro();

            }

        });
       
        trsFiltro = new TableRowSorter(txtDatas.getModel()); 
        txtDatas.setRowSorter(trsFiltro);

    }//GEN-LAST:event_searchBarKeyTyped
 public void filtro() {
        int columnaABuscar = 0;
        //Search by name
        if (comboFiltro.getSelectedItem().toString().equals("UserLogin")) {
            columnaABuscar = 0;
        }
        //search by lastname
        if (comboFiltro.getSelectedItem().toString().equals("UserModify")) {
            columnaABuscar = 1;
        }
        
        trsFiltro.setRowFilter(RowFilter.regexFilter("(?i)"+searchBar.getText(), columnaABuscar));
       
    }
 
 


